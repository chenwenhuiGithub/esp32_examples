static i2c_master_bus_handle_t s_hd_bus = NULL;
static i2c_master_dev_handle_t s_hd_dev = NULL;
static uint8_t s_buf[1024] = {0};
static uint8_t s_pixel[128 * 8] = {0}; // ssd1306 not support read operation, so use RAM to save pixel values

static esp_err_t i2c_init() {
    esp_err_t err = ESP_OK;
    i2c_master_bus_config_t bus_cfg = {
        .i2c_port = CONFIG_I2C_PORT,
        .scl_io_num = CONFIG_I2C_PIN_SCL,
        .sda_io_num = CONFIG_I2C_PIN_SDA,
        .clk_source = I2C_CLK_SRC_DEFAULT,
        .glitch_ignore_cnt = 7,
        .flags.enable_internal_pullup = 1,
    };
    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_7,
        .device_address = CONFIG_I2C_DEV_ADDR,
        .scl_speed_hz = 400 * 1000,
    };

    err = i2c_new_master_bus(&bus_cfg, &s_hd_bus);
    err |= i2c_master_bus_add_device(s_hd_bus, &dev_cfg, &s_hd_dev);
    return err;
}

static esp_err_t ssd1306_write_cmd(const uint8_t *value, uint32_t len) {
    s_buf[0] = 0x00;
    memcpy(s_buf + 1, value, len);
    return i2c_master_transmit(s_hd_dev, s_buf, len + 1, -1);
}

static esp_err_t ssd1306_write_data(const uint8_t *value, uint32_t len) {
    s_buf[0] = 0x40;
    memcpy(s_buf + 1, value, len);
    return i2c_master_transmit(s_hd_dev, s_buf, len + 1, -1);
}

// x - 0~127, y - 0~7
static void oled_set_pos(uint8_t x, uint8_t y) {
    uint8_t cmd[3] = {
        (0xB0 + y),                 // start page address, 0xB0 + bit[2:0]
        (((x & 0xF0) >> 4) | 0x10), // start column address, higher part, 0x10 + bit[3:0]
        (x & 0x0F),                 // start column address, lower part, 0x00 + bit[3:0]
    };

    ssd1306_write_cmd(cmd, sizeof(cmd));
}

esp_err_t oled_init() {
    esp_err_t err = ESP_OK;;
    uint8_t cmd[23] = {
        0xAE, // display on/off, 0xAE - off, 0xAF - on
        0xA8, // set multiplex ratio, 0xA8 + bit[5:0]
        0x3F, // bit[5:0]
        0xD3, // set display offset, 0xD3 + bit[5:0]
        0x00, // bit[5:0]
        0x40, // set display RAM start line address, 0x40 + bit[5:0]
        0xA1, // set segment remap, 0xA0 + bit[0], 0xA0 - column 0 map to SEG0, 0xA1 - column 127 map to SEG0
        0xC8, // set Com output scan direction, 0xC0 + bit[3], 0xC0 - normal mode, 0xC8 - inverse mode
        0xDA, // set com pin configuartion, 0xDA + bit[5:4] + 0x02
        0x12, // bit[5:4] + 0x02
        0x81, // set contract control, 0x81 + bit[7:0]
        0x7F, // bit[7:0]
        0xA4, // set Entire Display ON, 0xA4 + bit[0], 0xA4 - resume RAM content, 0xA5 - ignore RAM content
        0xA6, // set normal/inverse display, 0xA6 + bit[0], 0xA6 - normal, 0xA7 - inverse
        0xD5, // set clk divide ratio/osc freq, 0xD5 + bit[7:0]
        0x80, // bit[7:0]
        0x8D, // set Charge Pump, 0x8D + 0x10 + bit[2]
        0x14, // 0x10 + bit[2]
        0xD9, // set Pre-Charge Period, 0xD9 + bit[7:0]
        0x22, // bit[7:0]
        0xDB, // set Vcomh deselect level, 0xDB + bit[6:4]
        0x20, // bit[6:4]
        0xAF, // display on/off, 0xAE - off, 0xAF - on
    };

    err = i2c_init();
    if (ESP_OK != err) {
        return err;
    }

    ssd1306_write_cmd(cmd, sizeof(cmd));

    return ESP_OK;
}

void oled_clear() {
    uint8_t i = 0;
    uint8_t data[128] = {0};

    for (i = 0; i < 8; i++) {
        oled_set_pos(0, i);
        ssd1306_write_data(data, sizeof(data));
    }
    memset(s_pixel, 0, sizeof(s_pixel));
}

void oled_show_char(uint8_t x, uint8_t y, uint8_t ch, char_size_t size) {
    uint8_t i = 0;

    ch -= ' ';
    if (CHAR_SIZE_6X8 == size) {
        for (i = 0; i < 6; i++) {
            s_pixel[y * 128 + x + i] = font6X8[ch][i];
        }
        oled_set_pos(x, y);
        ssd1306_write_data(&s_pixel[y * 128 + x], 6);
    } else {
        for (i = 0; i < 8; i++) {
            s_pixel[y * 128 + x + i] = font8X16[ch * 16 + i];
        }
        for (i = 0; i < 8; i++) {
            s_pixel[(y + 1) * 128 + x + i] = font8X16[ch * 16 + 8 + i];
        }
        oled_set_pos(x, y);
        ssd1306_write_data(&s_pixel[y * 128 + x], 8);
        oled_set_pos(x, y + 1);
        ssd1306_write_data(&s_pixel[(y + 1) * 128 + x], 8);
    }
}

void oled_show_string(uint8_t x, uint8_t y, char *string, char_size_t size) {
    uint8_t i = 0;

    while (string[i] != '\0') {
        oled_show_char(x, y, string[i], size);
        if (CHAR_SIZE_6X8 == size) {
            x += 6;
            if (x > 122) {
                x = 0;
                y += 1;
            }
        } else {
            x += 8;
            if (x > 120) {
                x = 0;
                y += 2;
            }
        }
        i++;
    }
}

void oled_show_chinese(uint8_t x, uint8_t y, uint8_t *index, uint8_t num) {
    uint8_t i = 0, j = 0;

    for (j = 0; j < num; j++) {
        for (i = 0; i < 16; i++) {
            s_pixel[y * 128 + x + i] = fontChinese[2 * index[j]][i];
        }
        for (i = 0; i < 16; i++) {
            s_pixel[(y + 1) * 128 + x + i] = fontChinese[2 * index[j] + 1][i];
        }
        oled_set_pos(x, y);
        ssd1306_write_data(&s_pixel[y * 128 + x], 16);
        oled_set_pos(x, y + 1);
        ssd1306_write_data(&s_pixel[(y + 1) * 128 + x], 16);

        x += 16;
        if (x > 112) {
            x = 0;
            y += 2;
        }
    }
}

void oled_show_point(uint8_t x, uint8_t y, point_stat_t stat) {
    uint8_t row = y / 8;
    uint8_t bit = y % 8;
    uint8_t mask = 0;

    if (POINT_STAT_OFF == stat) {
        mask = ~(1 << bit);
        s_pixel[row * 128 + x] &= mask;
    } else {
        mask = 1 << bit;
        s_pixel[row * 128 + x] |= mask;
    }
    oled_set_pos(x, row);
    ssd1306_write_data(&s_pixel[row * 128 + x], 1);
}

void oled_show_line(uint8_t x1, uint8_t y1, uint8_t x2, uint8_t y2, point_stat_t stat) {
    uint8_t x = 0, y = 0;

    if (x1 == x2) {
        for (y = y1; y <= y2; y++) {
            oled_show_point(x1, y, stat);
        }
        return;
    }

    if (y1 == y2) {
        for (x = x1; x <= x2; x++) {
            oled_show_point(x, y1, stat);
        }
        return;
    } 

    for (x = x1; x <= x2; x++) {
        y = y1 + ((((x - x1) * 1000) / (x2 - x1)) * (y2 - y1)) / 1000;
        oled_show_point(x, y, stat);
    }
}
